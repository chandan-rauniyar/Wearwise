<script>
    let swiper = new Swiper('.swiper', {
        loop: true,
        autoplay: {
            delay: 10000,
            disableOnInteraction: false,
        },
        speed: 500, // Smooth transition speed
        effect: "fade", // Smooth fade effect
        fadeEffect: {
            crossFade: true
        },
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev',
        },
        pagination: {
            el: '.swiper-pagination',
            clickable: true,
        },
        on: {
            slideChangeTransitionStart: function() {
                document.querySelectorAll(".swiper-slide").forEach(slide => {
                    slide.classList.remove("auto-zoom");
                });
            },
            slideChangeTransitionEnd: function() {
                if (!swiper.autoplay.running) {
                    swiper.autoplay.start();
                }
                swiper.slides[swiper.activeIndex].classList.add("auto-zoom");
            }
        }
    });

    // Prevent zoom effect on manual navigation
    document.querySelector('.swiper-button-next').addEventListener('click', () => swiper.autoplay.stop());
    document.querySelector('.swiper-button-prev').addEventListener('click', () => swiper.autoplay.stop());
</script>

